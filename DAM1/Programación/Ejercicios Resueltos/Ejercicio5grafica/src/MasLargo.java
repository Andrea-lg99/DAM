    static JToggleButton getBotonParaColorear (ButtonGroup bg, int comp)    
    {
        Enumeration <AbstractButton> botones = bg.getElements();
        
        while(botones.hasMoreElements())
        {
            JToggleButton boton = (JToggleButton) botones.nextElement();
            
            if(Integer.parseInt(boton.getText()) == comp)
            {
                return boton;
            }
        }
        
        return null;
    }
    
    private void convoyMasLargo ()
    {
        int [] contadorBarcos = new int [9]; 
        int mayor = contadorBarcos[0];
        int zona = 0;
        
        Enumeration listaBarcos = convoy.contenedor.elements();
        
        while (listaBarcos.hasMoreElements())
        {
            Barco barco = (Barco) listaBarcos.nextElement();
            contadorBarcos[convoy.calcularZona(barco.getPosicion()[0], barco.getPosicion()[1])-1] ++;
        }
        
        for (int i = 0; i < contadorBarcos.length; i++) 
        {
            if (contadorBarcos[i] > mayor)
            {
                mayor = contadorBarcos[i];
                zona = i + 1;
            }
        }
        
        for (int i = 0; i < contadorBarcos.length; i++)
        {
            if (mayor == contadorBarcos[i])
            {
                getBotonParaColorear(buttonGroup1, zona).setBackground(Color.red);
            }
        }
        
        txtMasLargo.setText("El convoy mas largo estÃ¡ en la zona: " + zona +
            " con " + contadorBarcos[zona - 1] + " barcos.");
        
    }
    
