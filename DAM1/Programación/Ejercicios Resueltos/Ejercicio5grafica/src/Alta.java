    private int[] calcularZona()
    {
        int[] campoXY = new int[2];
        
        for (int i = 0;i < convoy.zona.length;i++)
        {
            for (int j = 0;j < convoy.zona[i].length; j++)
            {
                if(convoy.zona [i][j] == zona)
                {
                    campoXY[0] = i + 1;
                    campoXY[1] = j + 1;
                }
            }
        }
        return campoXY;
    }
