    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        boolean vacio = false;
        int pasajeros = 0;
        
        if(txtNombre.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "El campo Nombre no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            vacio = true;
        }
        
        if(txtPasajeros.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "El campo Pasajeros no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            vacio = true;
        }
        
        if(zona == 0)
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una zona", "Error", JOptionPane.ERROR_MESSAGE);
            vacio = true;
        }
        
        if(vacio == false)
        {
            try
            {
                pasajeros = Integer.parseInt(txtPasajeros.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "Valor de pasajeros introducido no válido", "Error", JOptionPane.ERROR_MESSAGE);
                vacio = true;
            }
            
            if(vacio == false)
            {
                Barco barco = new Barco(txtNombre.getText(), pasajeros, campoX, campoY);
                String clave = barco.getNombre();
                
                if(!convoy.contenedor.containsKey(clave))
                    {
                        if(convoy.contenedor.put(clave, barco) != null)
                        {
                            JOptionPane.showMessageDialog(null, "ERROR al introducir el barco " + barco.getNombre(), "Error al introducir barco", JOptionPane.ERROR_MESSAGE);
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, barco.getNombre() + " introducido a la lista", "Correcto", JOptionPane.INFORMATION_MESSAGE);
                        }
                        
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "ERROR, este barco ya ha salido del puerto", "Error", JOptionPane.WARNING_MESSAGE);
                    }
            }
        }
        
    }//GEN-LAST:event_btnAnadirActionPerformed

