    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        boolean sw = true;
        String cad = "";

        if(txtL1.getText().equals(""))
        {
            cad = lado1.getText() +  " es obligatorio\n";
            sw = false;
        }

        if(txtL2.getText().equals(""))
        {
            cad += lado2.getText()+  " es obligatorio\n";
            sw = false;
        }

        if(txtL3.getText().equals(""))
        {
            cad += lado3.getText() +  " es obligatorio";
            sw = false;
        }

        if(sw == false)
        {
            JOptionPane.showMessageDialog(null, cad);
        }
        
        if(sw)
        {
            try
            {
                a = Integer.parseInt(txtL1.getText());
                b = Integer.parseInt(txtL2.getText());
                c = Integer.parseInt(txtL3.getText());
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Valor numérico no válido");
                sw = false;
            }
        }
        
        if(sw)
        {
            if(a <= 0 || b <= 0 || c <= 0)
            {
                sw = false;
                
                JOptionPane.showMessageDialog(null, "Los valores deben ser mayores a 0");
            }
        }
        
        if(sw)
        {
            if(a + b > c && a + c > b && b + c > a)
            {
                double p = (double) (a + b + c) / 2;
                
                s = (double) Math.round(Math.sqrt(p * (p - a) * (p - b) * (p - c)) * 1000d) / 1000d;
                txtResul.setText(Double.toString(s));
            }
            else
            {
                JOptionPane.showMessageDialog(null, "no existe triángulo");
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

