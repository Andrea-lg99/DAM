    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(txtMatricula.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Debe introducir una matrícula");
        }
        else
        {
            if(existeMatricula())
            {
                jPanel1.setVisible(true);
                btnBuscar.setEnabled(false);
                    
                try
                {
                    String consulta = "select precio from coches where matricula = '" + txtMatricula.getText() + "'";
                    ResultSet r = Principal.sentencia.executeQuery(consulta);
                    
                    r.next();
                    txtPactual.setText("El precio que tiene ahora es: " + r.getString("precio"));
                }
                catch (SQLException ex)
                {
                    JOptionPane.showMessageDialog(rootPane, "Error al obtener el precio " + ex);
                }
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int precio = 0;
        boolean correcto = true;
        
        try
        {
            precio = Integer.parseInt(txtPnuevo.getText());
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Introduce un precio válido");
            correcto = false;
        }
        
        if(correcto)
        {
            if(precio > 0)
            {
                try
                {
                    String consulta = "update coches set precio = " + precio + " where matricula = '" + txtMatricula.getText() + "'";
                    Principal.sentencia.executeUpdate(consulta);
                    JOptionPane.showMessageDialog(rootPane, "Precio actualizado");
                }
                catch (SQLException ex)
                {
                    JOptionPane.showMessageDialog(rootPane, "Error al modificar el precio " + ex);
                }

                jPanel1.setVisible(false);
                btnBuscar.setEnabled(true);
                txtPnuevo.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "El precio tiene que ser mayor a 0");
            }
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

