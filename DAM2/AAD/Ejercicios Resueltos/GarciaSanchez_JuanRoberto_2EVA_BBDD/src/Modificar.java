    boolean existeMatricula()
    {
        String matricula, consulta = "";
        matricula = txtMatricula.getText();
        
        try
        {
            consulta = "select matricula from coches where matricula = '" + matricula + "'";
            ResultSet r = Principal.sentencia.executeQuery(consulta);
            
            if(!r.first())
            {
                JOptionPane.showMessageDialog(rootPane, "Matrícula no dada de alta");
                return false;
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Error al buscar la matrícula " + e);
        }
        
        return true;
    }

