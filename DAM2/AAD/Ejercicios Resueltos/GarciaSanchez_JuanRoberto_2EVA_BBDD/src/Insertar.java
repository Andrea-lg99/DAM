    boolean chequearDatos()
    {
        boolean correcto = true;
        String cad = "";
        
        if(txtMatricula.getText().isEmpty())
        {
            cad += "Introduce una matrícula\n";
            correcto = false;
        }
        
        if(txtMarca.getText().isEmpty())
        {
            cad += "Introduce una marca\n";
            correcto = false;
        }
        
        if(txtPrecio.getText().isEmpty())
        {
            cad += "Introduce un precio\n";
            correcto = false;
        }
        
        
        if(txtDni.getText().isEmpty())
        {
            cad += "Introduce un DNI\n";
            correcto = false;
        }
        
        if(correcto)
        {
            try
            {
                if(Integer.parseInt(txtPrecio.getText()) < 0)
                {
                    JOptionPane.showMessageDialog(rootPane, "El precio tiene que ser positivo");
                    correcto = false;
                }
            }
            catch(NumberFormatException e)
            {
                correcto = false;
                JOptionPane.showMessageDialog(rootPane, "El precio no es un valor válido");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, cad);
        }
        
        return correcto;
    }
