    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(chequearDatos())
        {
            String consulta = "";
        
            try
            {
                consulta += "insert into coches (matricula, marca, precio, dni) values (";
                consulta += "'" + txtMatricula.getText() + "',";
                consulta += "'" + txtMarca.getText() + "',";
                consulta += "'" + txtPrecio.getText() + "',";
                consulta += "'" + txtDni.getText() + "')";

                Principal.sentencia.executeUpdate(consulta);
                JOptionPane.showMessageDialog(rootPane, "Coche añadido correctamente");
            }
            catch(SQLException e)
            {
                if(e.getErrorCode() == 1062)
                {
                    JOptionPane.showMessageDialog(rootPane, "Error al introducir el nuevo coche\n Matrícula duplicada");
                }
                else if(e.getErrorCode() == 1452)
                {
                    txtArea.setText("DNI no existe.\n\n Antes debe dar de alta al propietario");
                    jPanel1.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Error al introducir el nuevo coche " + e);
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

