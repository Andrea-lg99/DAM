    private void btnContMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContMatActionPerformed
        existe = false;
        
        for(int i = 0; i < Principal.avis.length; i++)
        {
            
            if(txtMatri.getText().equalsIgnoreCase(Principal.avis[i].matricula))
            {
                existe = true;
                pos = i;
                
                if(Principal.avis[i] instanceof Camion)
                {
                    JOptionPane.showMessageDialog(null, "Esta matrícula pertenece a un camión", "Error", JOptionPane.WARNING_MESSAGE);
                }
                else if(Principal.avis[i].alquilado)
                {
                    JOptionPane.showMessageDialog(null, "El coche " + Principal.avis[i].matricula + " ya está alquilado", "Error", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    txtMatri.setEnabled(false);
                    btnContMat.setEnabled(false);
                    jPanel2.setVisible(true);
                }
            }
        }
        
        if(! existe)
        {
            JOptionPane.showMessageDialog(null, "Este coche no existe en la empresa", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnContMatActionPerformed

    private void btnContKmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContKmActionPerformed
        existe = true;
        
        try
        {
            km = Integer.parseInt(txtKm.getText());
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Formato de Km no válido", "Error", JOptionPane.ERROR_MESSAGE);
            existe = false;
        }
        
        if(existe)
        {
            Principal.avis[pos].alquilar(km);
            Principal.numTurAlquilados++;
            JOptionPane.showMessageDialog(null, "Total turismos: " + Principal.numTurismos + " Alquilados: " + Principal.numTurAlquilados, "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btnContKmActionPerformed

