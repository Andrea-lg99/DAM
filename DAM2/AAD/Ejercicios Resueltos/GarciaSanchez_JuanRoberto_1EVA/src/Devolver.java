    private void btnContMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContMatActionPerformed
        for(int i = 0; i < Principal.avis.length; i++)
        {
            if(txtMatri.getText().equalsIgnoreCase(Principal.avis[i].matricula))
            {
                existe = true;
                pos = i;

                if(Principal.avis[i] instanceof Camion)
                {
                    txtTipo.setText("CAMIÓN");
                }
                else
                {
                    txtTipo.setText("TURISMO");
                }
                
                if(! Principal.avis[i].alquilado)
                {
                    JOptionPane.showMessageDialog(null, "El vehículo " + Principal.avis[i].matricula + " no está alquilado", "Error", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    if(Principal.avis[i] instanceof Turismo)
                    {
                        txtKmAnt.setText(String.valueOf("Km anterior: " + ((Turismo)Principal.avis[i]).kmAlquiler));
                    }
                    else
                    {
                        txtKmAnt.setText(String.valueOf("Día anterior: " + ((Camion)Principal.avis[i]).diaAlquiler));
                    }
                    
                    btnContMat.setEnabled(false);
                    jPanel2.setVisible(true);
                }
            }
        }

        if(! existe)
        {
            JOptionPane.showMessageDialog(null, "Este coche no existe en la empresa", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnContMatActionPerformed

    private void btnContKmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContKmActionPerformed
        existe = true;
        
        try
        {
            km = Integer.parseInt(txtKmAct.getText());
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Formato de Km/día no válido", "Error", JOptionPane.ERROR_MESSAGE);
            existe = false;
        }

        if(existe)
        {
            Principal.avis[pos].devolver(km);
            
            if(Principal.avis[pos] instanceof Turismo)
            {
                Principal.numTurAlquilados--;
                
                if(!Principal.miACoche.isEnabled())
                {
                    Principal.miACoche.setEnabled(true);
                }
                
            }
            else
            {
                Principal.numCamAlquilados--;
                
                if(!Principal.miACamion.isEnabled())
                {
                    Principal.miACamion.setEnabled(true);
                }
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_btnContKmActionPerformed

