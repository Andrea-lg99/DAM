    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String nombre = null, categoria = null, cad = "";
        double peso = 0;
        double precio = 0;
        boolean b = true;
        
        if(txtNombre.getText().isEmpty())
        {
            cad += "Falta del nombre\n";
            b = false;
        }
        
        if(txtCategoria.getText().isEmpty())
        {
            cad += "Falta la categoría\n";
            b = false;
        }
        
        if(txtPeso.getText().isEmpty())
        {
            cad += "Falta el peso\n";
            b = false;
        }
        
        if(txtPkilo.getText().isEmpty())
        {
            cad += "Falta el precio";
            b = false;
        }
        
        if(! b)
        {
            JOptionPane.showMessageDialog(null, cad, "Error", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            try
            {
                nombre = txtNombre.getText();
                categoria = txtCategoria.getText();
                peso = Double.parseDouble(txtPeso.getText());
                precio = Double.parseDouble(txtPkilo.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "Formato de datos incorrecto en peso o precio", "Error", JOptionPane.ERROR_MESSAGE);
                b = false;
            }
            
            if(b)
            {
                ProductoPeso porpeso = new ProductoPeso(peso, precio, nombre, categoria);
                Grafica.productos.add(porpeso);
                JOptionPane.showMessageDialog(null, "Producto añadido correctamente", "Correcto", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

