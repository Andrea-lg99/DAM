    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        double total = 0;
        txtTicket.append("TICKET DE LA COMPRA\t Cliente: " + Grafica.nombre + "\n\n");
        
        for(int i = 0; i < Grafica.productos.size(); i++)
        {
            txtTicket.append(Grafica.productos.get(i).imprimir());
            total += Grafica.productos.get(i).getImporteTotal();
        }
        
        txtTicket.append("\nIMPORTE TOTAL: " + total);
    }//GEN-LAST:event_btnMostrarActionPerformed

