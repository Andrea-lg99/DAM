    private void btnOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrigenActionPerformed
        int resp;
        fcSelecFichero.setApproveButtonText("Seleccionar");
        
        resp = fcSelecFichero.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichOri = fcSelecFichero.getSelectedFile().toString();
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {

            JOptionPane.showMessageDialog(null,"Operación cancelada");
        }
        
        btnDestino.setVisible(true);
        
        txtAOrigen.setText(fichOri);
        miArchivo.mostrar(fichOri, txtArea);
    }//GEN-LAST:event_btnOrigenActionPerformed

    private void btnDestinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDestinoActionPerformed
        int resp;
        fcSelecFichero.setApproveButtonText("Guardar");
        
        resp = fcSelecFichero.showOpenDialog(this);
        

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichEnc = fcSelecFichero.getSelectedFile().toString();
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {

            JOptionPane.showMessageDialog(null,"Operación cancelada");
        }
        
        jPanel2.setVisible(true);
    }//GEN-LAST:event_btnDestinoActionPerformed

    private void btnEncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncActionPerformed
        miArchivo.encriptar(fichEnc, txtArea.getText());
        txtADestino.setText(fichEnc);
        miArchivo.mostrar(fichEnc, txtArea1);
        jPanel3.setVisible(true);
    }//GEN-LAST:event_btnEncActionPerformed

