    private void btnVerSerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerSerActionPerformed
        String info = "";
        String fecha, dia, mes, ano, coste;
        
        try
        {
            ResultSet r = sentencia.executeQuery("select * from servicios order by cantidad");
            r.beforeFirst();
            
            while(r.next())
            {
                fecha = r.getString("fecha");
                ano = fecha.substring(0, 4);
                mes = fecha.substring(5, 7);
                dia = fecha.substring(8, 10);
                fecha = dia + "/" + mes + "/" + ano;
                
                coste = r.getString("cantidad");
                coste = coste.replace(".", ",");
                
                info = info + fecha + " " + r.getString("tipo") + " " + coste + "\n";
            }
            
            JOptionPane.showMessageDialog(null, info);
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error al consultar la tabla servicios" + ex);
        }

    }//GEN-LAST:event_btnVerSerActionPerformed

