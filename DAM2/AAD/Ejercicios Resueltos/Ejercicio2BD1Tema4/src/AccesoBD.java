    private void btnVerCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerCliActionPerformed
        String info = "";
        String nomcli, tfno1, tfno2;
        
        try
        {
            ResultSet r = sentencia.executeQuery("select * from clientes order by nombre");
            r.beforeFirst();
            
            while(r.next())
            {
                nomcli = r.getString("nombre");
                tfno1 = r.getString("tfno1");
                if(tfno1.isEmpty())
                {
                    tfno1 = "no tiene";
                }
                
                tfno2 = r.getString("tfno2");
                if(tfno2.isEmpty())
                {
                    tfno2 = "no tiene";
                }
                
                info = info + nomcli + " ---  Fijo: " + tfno1 + " Móvil: " + tfno2 + "\n";
            }
            
            JOptionPane.showMessageDialog(null, info);
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error al consultar la tabla clientes" + ex);
        }
    }//GEN-LAST:event_btnVerCliActionPerformed

