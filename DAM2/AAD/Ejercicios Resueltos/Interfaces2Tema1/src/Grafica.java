    private void btnSeguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguirActionPerformed
        int dimension = 0;
        boolean b = true;
        
        txtVector.setText("");
        txtMax.setText("");
        txtMin.setText("");
        txtSuma.setText("");
        
        try
        {
            dimension = Integer.parseInt(txtDimen.getText());
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Introduce un número", "Error", JOptionPane.ERROR_MESSAGE);
            b = false;
        }
        
        if(b)
        {
            double vector[] = new double[dimension];
            ArrayReales reales = new ArrayReales();
            
            reales.valor = vector;
            reales.asignar();
            
            for(int i = 0; i < vector.length; i++)
            {
                txtVector.append("x[" + i + "]= " + String.valueOf(reales.valor[i]) + "\n");
            }
            
            txtMax.setText(String.valueOf(reales.maximo()));
            txtMin.setText(String.valueOf(reales.minimo()));
            txtSuma.setText(String.valueOf(reales.sumatorio()));
            
            jPanel1.setVisible(true);
        }
    }//GEN-LAST:event_btnSeguirActionPerformed

