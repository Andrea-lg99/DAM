    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String nombre = txtNombre.getText();
        String nif = txtNIF.getText();
        String turno = txtTurno.getText();
        String cad = "";
        int vac = 0; 
        double salario = 0;
        boolean valido = true;
        
        if(txtNombre.getText().isEmpty())
        {
            cad += "Falta el nombre\n";
            valido = false;
        }
        
        if(txtNIF.getText().isEmpty())
        {
            cad += "Falta el NIF\n";
            valido = false;
        }
        
        if(txtVac.getText().isEmpty())
        {
            cad += "Faltan los días de vacaciones\n";
            valido = false;
        }
        
        if(txtSalario.getText().isEmpty())
        {
            cad += "Falta el salario\n";
            valido = false;
        }
        
        if(txtTurno.getText().isEmpty())
        {
            cad += "Falta el turno\n";
            valido = false;
        }
        
        if(! valido)
        {
            JOptionPane.showMessageDialog(null, cad, "Error", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            try
            {
                vac = Integer.parseInt(txtVac.getText());
            }
            catch (NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "Días de vacaciones no válido", "Error", JOptionPane.ERROR_MESSAGE);
                valido = false;
            }
            
            try
            {
                salario = Double.parseDouble(txtSalario.getText());
            }
            catch (NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "El salacio base no es válido", "Error", JOptionPane.ERROR_MESSAGE);
                valido = false;
            }
            
            if(valido)
            {
                EmpleadoProduccion produccion = new EmpleadoProduccion(nombre, nif, vac, salario, turno);
                Grafica.listaEmpleados.add(produccion);
                JOptionPane.showMessageDialog(null, "Empleado añadido con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

