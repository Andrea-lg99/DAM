    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
        
        if(miArchivo.estaPrestado(txtDirFpres.getText(), Integer.parseInt(txtCodigo.getText())))
        {
            jPanel2.setVisible(false);
            jPanel1.setVisible(true);
        }
        else
        {
            Prestados prestado = new Prestados(Integer.parseInt(txtCodigo.getText()));
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            
            miArchivo.anadirPrestado(txtDirFpres.getText(), prestado);
            
            txtDesde.setText(formato.format(prestado.Fecha_prestamo));
            txtHasta.setText(formato.format(prestado.Fecha_devolucion));
            jPanel1.setVisible(false);
            jPanel2.setVisible(true);
        }
    }//GEN-LAST:event_btnPrestarActionPerformed

