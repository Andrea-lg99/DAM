    private void btnElegirLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirLibrosActionPerformed
        int resp = fcSelecFichero.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            //JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichLibros = fcSelecFichero.getSelectedFile().toString();

            miArchivo.llenarTabla1(fichLibros, m, tabla);

            jPanel3.setVisible(true);
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {
            JOptionPane.showMessageDialog(null,"Operación cancelada");
        }
    }//GEN-LAST:event_btnElegirLibrosActionPerformed

    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        int fila = tabla.getSelectedRow();
        
        if(fila == -1)
        {
            JOptionPane.showMessageDialog(rootPane, "No se ha hecho selección");
        }
        else
        {
            int codigo = (Integer)tabla.getValueAt(fila, 0);
            String titulo = String.valueOf(tabla.getValueAt(fila, 1));
            
            ActualizarPrestados prestamos = new ActualizarPrestados();
            
            prestamos.txtDirFpres.setText(fichPres);
            prestamos.txtCodigo.setText(String.valueOf(codigo));
            prestamos.txtTitulo.setText(titulo);
            
            prestamos.setVisible(true);
        }
    }//GEN-LAST:event_btnContinuarActionPerformed

    private void btnElegirPrestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirPrestActionPerformed
        int resp = fcSelecFichero.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            //JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichPres = fcSelecFichero.getSelectedFile().toString();

            jPanel2.setVisible(true);
            jPanel1.setVisible(false);
            jPanel3.setVisible(false);
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {
            JOptionPane.showMessageDialog(null,"Operación cancelada");
        }
    }//GEN-LAST:event_btnElegirPrestActionPerformed

