    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String cad = "";
        int cod = 0;
        int horas = 0;
        int pos = 0;
        boolean correcto = true;
        boolean existe = false;

        if(txtCod.getText().isEmpty())
        {
            cad += "Introduce un código\n";
            correcto = false;
        }
        
        if(txtHoras.getText().isEmpty())
        {
            cad += "Introduce las horas";
            correcto = false;
        }

        if(correcto)
        {
            
            try
            {
                cod = Integer.parseInt(txtCod.getText());
                
                horas = Integer.parseInt(txtHoras.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "Formato incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
                correcto = false;
            }

            if(correcto)
            {
                for(int i = 0; i < Grafica.lista.size(); i++)
                {
                    if(cod == Grafica.lista.get(i).getIdentificador())
                    {
                        existe = true;
                        pos = i;
                    }
                }

                if(!existe)
                {
                    JOptionPane.showMessageDialog(null, "Este código no está dado de alta");
                }
                else if(Grafica.lista.get(pos).isFinalizado())
                {
                    JOptionPane.showMessageDialog(null, "Este trabajo está finalizado y no puede incrementar horas");
                }
                else if(horas < 0)
                {
                    JOptionPane.showMessageDialog(null, "Las Horas no pueden ser negativas");
                }
                else
                {
                    txtHant.setText(String.valueOf(Grafica.lista.get(pos).getNum_horas()));
                    Grafica.lista.get(pos).setNum_horas(horas);
                    txtHact.setText(String.valueOf(Grafica.lista.get(pos).getNum_horas()));
                    jPanel1.setVisible(true);
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, cad);
        }

    }//GEN-LAST:event_btnAddActionPerformed

