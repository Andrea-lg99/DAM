    private void btnFinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinActionPerformed
        int cod = 0;
        int pos = 0;
        boolean estAnte = false;
        boolean correcto = true;
        boolean existe = false;
        
        if(txtCod.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Introduce un c칩digo");
            correcto = false;
        }
        
        if(correcto)
        {
            try
            {
                cod = Integer.parseInt(txtCod.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null, "Formato incorrecto", "Error", JOptionPane.ERROR);
                correcto = false;
            }
            
            if(correcto)
            {
                for(int i = 0; i < Grafica.lista.size(); i++)
                {
                    if(cod == Grafica.lista.get(i).getIdentificador())
                    {
                        existe = true;
                        estAnte = Grafica.lista.get(i).isFinalizado();
                        pos = i;
                    }
                }
                
                if(!existe)
                {
                    JOptionPane.showMessageDialog(null, "Este c칩digo no est치 dado de alta");
                }
                else
                {
                    if(estAnte)
                    {
                        JOptionPane.showMessageDialog(null, "Este trabajo ya estaba finalizado");
                    }
                    else
                    {
                        Grafica.lista.get(pos).setFinalizado(true);
                    }
                    
                    if(estAnte)
                    {
                        txtAnte.setText("Finalizado");
                    }
                    else
                    {
                        txtAnte.setText("No finalizado");
                    }
                    
                    if(Grafica.lista.get(pos).isFinalizado())
                    {
                        txtActu.setText("Finalizado");
                    }
                    else
                    {
                        txtActu.setText("No finalizado");
                    }
                    
                    jPanel1.setVisible(true);
                }
            }
        }
        
    }//GEN-LAST:event_btnFinActionPerformed

